#Accessing values using keys

person = {
    "name": "Ali",
    "age": 25
}

print(person["name"])  # Output: Ali
print(person["age"])   # Output: 25


car = {
    "brand": "Toyota",
    "model": "Corolla",
    "year": 2022
}

print(car["brand"])  # Output: Toyota
print(car["model"])  # Output: Corolla  

